    public function index($id)
    {
        
        $user = User::findOrFail($id);
        
        return view("achivement" , ["user"=> $user , "achievements" =>  $this->achievements]);
    }

    public function updateAchievements(Request $request, $id)
    {
        $user = User::findOrFail($id);

        // Get selected achievements from the request
        $selectedAchievements = $request->input('achievements', []);
    
        // Map selected achievements to the corresponding badges
        $userAchievements = collect($this->achievements)
            ->filter(function ($achievement) use ($selectedAchievements) {
                return in_array($achievement['name'], $selectedAchievements);
            })
            ->mapWithKeys(function ($achievement) {
                return [$achievement['name'] => $achievement['badge']];
            })
            ->toArray();
    
        // Save achievements to the user's profile
        $user->achievements = $userAchievements;
        $user->save();
    
        

        return response()->json(['success' => 'Achievements updated successfully.']);
    }
    public function editAchievements($id)
    {
        $user = User::findOrFail($id);
        return view('user.edit_achievements', ['user' => $user]);
    }

    public function showAchievements($id)
    {
        $user = User::findOrFail($id);
        return view('profile', ['user' => $user,  'achievements' => $this->achievements]);
    }


   
